// Group by category helper
export const getPayloadsByCategory = () => {
  const grouped = {};
  Object.values(PAYLOAD_CATEGORIES).forEach(category => {
    grouped[category] = PAYLOAD_TYPES.filter(type => type.category === category);
  });
  return grouped;
};

// Search helper
export const searchPayloadTypes = (query) => {
  const lowerQuery = query.toLowerCase();
  return PAYLOAD_TYPES.filter(type => 
    type.label.toLowerCase().includes(lowerQuery) ||
    type.description.toLowerCase().includes(lowerQuery) ||
    type.category.toLowerCase().includes(lowerQuery)
  );
};